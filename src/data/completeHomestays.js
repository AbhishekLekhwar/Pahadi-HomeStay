// Helper function to get homestays by destination
export const getHomestaysByDestination = (destinationName) => {
  const normalizedName = destinationName.toLowerCase().replace(/\s+/g, "");
  return completeHomestays[normalizedName] || [];
};

// Helper function to get all homestays
export const getAllHomestays = () => {
  return Object.values(completeHomestays).flat();
};

// Helper function to get homestays by price range
export const getHomestaysByPriceRange = (minPrice, maxPrice) => {
  return getAllHomestays().filter(
    (homestay) => homestay.price >= minPrice && homestay.price <= maxPrice
  );
};

// Helper function to get homestays by rating
export const getHomestaysByRating = (minRating) => {
  return getAllHomestays().filter((homestay) => homestay.rating >= minRating);
};
